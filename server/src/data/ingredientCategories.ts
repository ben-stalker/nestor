// Sort longest-key first so more-specific entries take priority.
const ORDERED_ENTRIES: [string, string][] = [...CATEGORY_ENTRIES].sort(
  ([a], [b]) => b.length - a.length,
);

/**
 * Given an ingredient name, returns a shopping-list category string.
 * Matching is case-insensitive substring search; the first match wins.
 * Falls back to "Other" when no entry matches.
 */
export default function categoriseIngredient(name: string): string {
  const lower = name.toLowerCase().trim();
  const match = ORDERED_ENTRIES.find(([key]) => lower.includes(key));
  return match ? match[1] : 'Other';
}
